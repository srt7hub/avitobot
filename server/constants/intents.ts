/**
 * Проверяет, является ли сообщение запросом статуса оплаты.
 * Учитывает: точные фразы, комбинацию слов (оплат + брон), короткие пост-оплатные фразы.
 * Вопросы «как/где/когда» — пропускает в AI (это не «проверьте статус», а уточнение).
 */
export function isPaymentStatusIntent(textLower: string): boolean {
  const trimmed = textLower.trim()

  // Вопросы — передаём в AI, не отвечаем шаблоном
  const hasQuestionWord = ['как ', 'где ', 'когда ', 'почему ', 'сколько '].some(w => textLower.includes(w))
  if (hasQuestionWord) return false

  // Сообщение о другой теме — не перехватываем
  const hasOffTopicWord = ['парковк', 'wifi', 'вай-фай', 'заселен', 'заселить'].some(w => textLower.includes(w))
  if (hasOffTopicWord) return false

  // Точные фразы
  if (PAYMENT_STATUS_PHRASES.some(p => textLower.includes(p))) return true
  // Комбинация: «оплат*» + «брон*» рядом
  const hasPaymentWord = textLower.includes('оплат') || textLower.includes('предоплат')
  const hasBookingWord = textLower.includes('брон')
  if (hasPaymentWord && hasBookingWord) return true
  // Короткие фразы (всё сообщение = одна фраза)
  if ((SHORT_POST_PAYMENT_PHRASES as readonly string[]).includes(trimmed)) return true
  return false
}

/**
 * Проверяет, спрашивает ли гость про SMS.
 */
export function isSmsIntent(textLower: string): boolean {
  return SMS_PHRASES.some(p => textLower.includes(p))
}

/**
 * Проверяет, является ли сообщение коротким acknowledgment (ок, спасибо и т.п.).
 */
export function isAckIntent(textLower: string): boolean {
  const trimmed = textLower.trim()
  if (trimmed.length >= 40) return false
  // startsWith исключён намеренно: "спасибо за информацию по парковке" не должно быть ack
  return ACK_PHRASES.some(p => trimmed === p || trimmed.endsWith(' ' + p) || trimmed === p + '!')
}
