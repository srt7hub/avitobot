// Маскировка персональных данных (ПДн) гостя перед сохранением в БД и
// пересылкой во внешние сервисы (Telegram). Защита по 152-ФЗ: сырые телефоны
// и email не должны оседать в нашей базе или утекать в мессенджер.
//
// ВАЖНО: маскировку НЕ применяем к тексту, который идёт в AI-модель — там нужен
// оригинал, чтобы бот корректно реагировал (например, отказался уходить в
// мессенджер). Сам ответ бота отдельно чистится в responseFilter.

// Российские/международные телефоны: +7/8 (xxx) xxx-xx-xx и слитные 11 цифр.
const PHONE_REGEX = /(\+?[78][\s\-]?\(?\d{3}\)?[\s\-]?\d{3}[\s\-]?\d{2}[\s\-]?\d{2})/g
const BARE_11_DIGITS_REGEX = /\b\d{11}\b/g
const EMAIL_REGEX = /\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}\b/g

/**
 * Заменяет телефоны и email в тексте на плейсхолдеры.
 * Возвращает очищенную строку. Прочий текст сохраняется как есть.
 */
export function maskPii(text: string): string {
  if (!text) return text
  return text
    .replace(EMAIL_REGEX, '[email]')
    .replace(PHONE_REGEX, '[телефон]')
    .replace(BARE_11_DIGITS_REGEX, '[телефон]')
}
